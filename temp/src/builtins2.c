#include "../inc/minishell.h"

void	ms_echo(char **args)
{
	int	i;

	i = 1;
	while (args[i])
	{
		ft_printf("%s", args[i]);
		if (args[i + 1])
			ft_printf(" ");
		++i;
	}
	ft_printf("\n");
}
